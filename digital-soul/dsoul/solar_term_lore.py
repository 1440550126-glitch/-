def terms() -> list:
    return list(_LORE.keys())


def detect_term(utterance) -> str:
    u = str(utterance or "")
    for t in _LORE:                                # 节气名都两字、互不为子串，直接找
        if t in u:
            return t
    return ""


def lore(term) -> str:
    """某个节气的讲究：含义 + 习俗 + 农谚。认不出返回空。"""
    t = term if term in _LORE else detect_term(term)
    row = _LORE.get(t)
    if not row:
        return ""
    meaning, custom, proverb = row
    return f"{t}：{meaning}习俗——{custom}农谚：「{proverb}」"


def is_term_lore_query(utterance) -> bool:
    u = str(utterance or "")
    if not detect_term(u):
        return False
    return any(k in u for k in ("讲究", "习俗", "吃什么", "吃啥", "由来", "来历", "谚语",
                                "什么意思", "啥意思", "有啥说法", "要注意", "怎么过",
                                "干什么", "干啥", "是什么节气"))
