def is_getting_around_query(utterance, config=None) -> bool:
    """是不是在问出行/坐车/问路怎么弄。"""
    u = str(utterance or "")
    if any(k in u for k in ("出门", "出行", "走丢", "迷路", "防走丢")):
        return True
    m = find_mode(u, config)
    if m and any(k in u for k in ("怎么", "咋", "如何", "教教", "教我", "不会", "怎样", "流程", "步骤")):
        return True
    return False


def count(config=None) -> int:
    return len(_all(config))
