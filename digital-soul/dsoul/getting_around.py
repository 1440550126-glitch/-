def _all(config=None) -> dict:
    d = dict(_MODES)
    cfg = (config or {}).get("getting_around") if isinstance(config, dict) else None
    extra = (cfg or {}).get("modes") if isinstance(cfg, dict) else None
    if isinstance(extra, dict):
        for k, v in extra.items():
            if isinstance(v, (list, tuple)) and len(v) >= 2:
                d[str(k)] = (str(v[0]), str(v[1]))
            elif isinstance(v, dict) and v.get("steps"):
                d[str(k)] = (str(v["steps"]), str(v.get("tip", "")))
    return d


def modes(config=None) -> list:
    return list(_all(config).keys())


def find_mode(utterance, config=None):
    """从话里听出问的哪种出行方式（别名最长匹配）。听不出返回 None。"""
    u = str(utterance or "")
    for word in sorted(_ALIAS, key=len, reverse=True):
        if word in u:
            return _ALIAS[word]
    for m in _all(config):
        if m in u:
            return m
    return None


def how_to(mode, config=None) -> str:
    """某种出行方式怎么操作（步骤 + 提醒）。查不到返回空。"""
    d = _all(config)
    key = _ALIAS.get(str(mode or ""), str(mode or ""))
    if key not in d:
        return ""
    steps, tip = d[key]
    return f"{key}怎么坐：{steps}" + (f"（提醒：{tip}）" if tip else "")


