"""亲戚称呼计算器：给一条关系路径（"我爸的弟弟""妈妈的爸爸"），算出该叫什么（叔叔/外公）。

中国式称谓讲究父系/母系、长幼、内外，这里覆盖最常用的一批。纯逻辑、零依赖、可单测。
基本关系记号：f父 m母 H夫 W妻 OB哥 YB弟 OS姐 YS妹 S子 D女。
"""

from __future__ import annotations

# 把口语词映射到基本关系记号（多字优先匹配）
_TOKENS = [
    ("丈夫", "H"), ("老公", "H"), ("媳妇儿", "W"), ("老婆", "W"), ("妻子", "W"),
    ("闺女", "D"), ("女儿", "D"), ("儿子", "S"),
    ("爸爸", "f"), ("爹", "f"), ("父亲", "f"), ("爸", "f"),
    ("妈妈", "m"), ("娘", "m"), ("母亲", "m"), ("妈", "m"),
    ("哥哥", "OB"), ("哥", "OB"), ("兄", "OB"),
    ("弟弟", "YB"), ("弟", "YB"),
    ("姐姐", "OS"), ("姐", "OS"), ("姊", "OS"),
    ("妹妹", "YS"), ("妹", "YS"),
    ("夫", "H"), ("妻", "W"), ("子", "S"), ("女", "D"), ("父", "f"), ("母", "m"),
]

# 关系路径 → 称呼（取最常见叫法）
_TERMS = {
    ("f",): "爸爸", ("m",): "妈妈", ("H",): "丈夫", ("W",): "妻子",
    ("OB",): "哥哥", ("YB",): "弟弟", ("OS",): "姐姐", ("YS",): "妹妹",
    ("S",): "儿子", ("D",): "女儿",
    ("f", "f"): "爷爷", ("f", "m"): "奶奶",
    ("m", "f"): "外公", ("m", "m"): "外婆",
    ("f", "OB"): "伯伯", ("f", "YB"): "叔叔", ("f", "OS"): "姑姑", ("f", "YS"): "姑姑",
    ("m", "OB"): "舅舅", ("m", "YB"): "舅舅", ("m", "OS"): "姨妈", ("m", "YS"): "姨妈",
    ("OB", "S"): "侄子", ("YB", "S"): "侄子", ("OB", "D"): "侄女", ("YB", "D"): "侄女",
    ("OS", "S"): "外甥", ("YS", "S"): "外甥", ("OS", "D"): "外甥女", ("YS", "D"): "外甥女",
    ("S", "S"): "孙子", ("S", "D"): "孙女", ("D", "S"): "外孙", ("D", "D"): "外孙女",
    ("f", "f", "f"): "曾祖父", ("f", "f", "m"): "曾祖母",
    ("S", "W"): "儿媳", ("D", "H"): "女婿",
    ("W", "f"): "岳父", ("W", "m"): "岳母", ("H", "f"): "公公", ("H", "m"): "婆婆",
    ("f", "OB", "S"): "堂哥/堂弟", ("f", "YB", "S"): "堂弟",
    ("f", "OS", "S"): "表哥/表弟", ("m", "OB", "S"): "表哥/表弟",
    ("OB", "W"): "嫂子", ("YB", "W"): "弟媳", ("OS", "H"): "姐夫", ("YS", "H"): "妹夫",
}


def parse_steps(text) -> list:
    """把"我爸爸的弟弟"解析成 ['f','YB']。识别不了的字会被跳过。"""
    t = str(text or "")
    for junk in ("我", "的", "他", "她", "你", "您", "之", " "):
        t = t.replace(junk, "")
    steps, i = [], 0
    while i < len(t):
        for word, code in _TOKENS:
            if t.startswith(word, i):
                steps.append(code)
                i += len(word)
                break
        else:
            i += 1
    return steps


def term_for(steps) -> str | None:
    """关系路径 → 称呼；查不到返回 None。"""
    key = tuple(steps or [])
    return _TERMS.get(key)


def call_what(text) -> str:
    """口语进、称呼出：'我爸的弟弟' → '叫叔叔'。"""
    steps = parse_steps(text)
    if not steps:
        return "这关系我没听明白，换个说法？比如「我妈的哥哥」。"
    term = term_for(steps)
    if term:
        return f"该叫{term}。"
    return "这门亲戚有点绕，我一时算不准，要不你说得再细点？"
