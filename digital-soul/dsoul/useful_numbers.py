def all_numbers() -> list:
    return [(n, num, when) for n, num, when, _k in _NUMBERS]


def emergency() -> str:
    """一口气把救命的几个号报清楚。"""
    return "记牢这几个：报警 110、火警 119、急救 120、交通事故 122、反诈 96110。"


def number_for(query) -> str:
    """按情形给号码（"着火打几"→119）。对不上返回空。"""
    u = str(query or "")
    if not u:
        return ""
    best, blen = None, 0
    for name, num, when, keys in _NUMBERS:
        for k in keys:
            if k in u and len(k) > blen:
                best, blen = (name, num, when), len(k)
    if not best:
        return ""
    name, num, when = best
    return f"{name}打 {num}（{when}）。"


def is_number_query(utterance) -> bool:
    u = str(utterance or "")
    if any(k in u for k in ("报警电话", "急救电话", "火警电话", "反诈电话", "投诉电话",
                            "求助电话", "应急电话", "常用电话", "号码是多少")):
        return True
    # "着火/被骗…打哪个电话/打几"
    if any(k in u for k in ("打哪个", "打几", "打多少", "拨打", "打什么号")) and number_for(u):
        return True
    return False
