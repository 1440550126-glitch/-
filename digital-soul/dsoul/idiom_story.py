def idioms() -> list:
    return list(_IDIOMS.keys())


def find(utterance):
    u = utterance or ""
    for name in _IDIOMS:
        if name in u:
            return name
    return None


def is_idiom_query(utterance) -> bool:
    u = utterance or ""
    return any(k in u for k in ("的故事", "什么意思", "啥意思", "怎么来的", "的来历", "讲讲",
                                "什么典故", "成语故事")) and find(u) is not None


def tell(name) -> str:
    d = _IDIOMS.get(name)
    if not d:
        return ""
    return f"《{name}》的故事——{d['story']} 这告诉咱：{d['moral']}"
