"""助眠：夜里睡不着、翻来覆去时，轻声陪你慢下来——松松身子、数数呼吸、想点温柔的事。
不是讲给孩子的睡前故事，也不是惊慌时的安抚，是那句"别急，我陪着你，安心睡"。

present-tense、纯逻辑、可单测。几种引导轮换着来，声音放软放慢最好。
"""

from __future__ import annotations

_SLEEPLESS = ("睡不着", "失眠", "翻来覆去", "睡不踏实", "睡不沉", "睡不好", "睡不香",
              "难入睡", "老是醒", "老醒", "睡不实", "怎么也睡不")

# 几种助眠引导（present-tense、轻、慢）
_GUIDES = [
    "来，跟着我——肩膀松下来，眉头舒展开，让身子整个陷进被窝里。"
    "慢慢吸气……再慢慢吐出去。白天的事都交给明天，这会儿什么都不用想。我陪着你，安心睡。",
    "别急着睡，越急越睡不着。咱们就慢慢数呼吸：吸气数一、吐气数二……数到十再从头。"
    "数着数着，身子越来越沉，眼皮越来越重，就这么睡过去。",
    "闭上眼，心里想个让你最安心的地方——也许是老家的院子、夏夜的风、远处一两声虫鸣。"
    "让心像水面一样，慢慢、慢慢地平下来。",
    "试个法子：鼻子吸气数四下，轻轻屏住数七下，再用嘴缓缓吐气数八下。"
    "来回几遍，心跳就慢下来了，人也松了。",
    "把今天放下吧。该做的都做了，没做完的明天还有大把工夫。"
    "现在只剩下这张床、这床被、还有我在旁边守着。踏踏实实地睡。",
]


def senses_sleepless(utterance) -> bool:
    return any(k in str(utterance or "") for k in _SLEEPLESS)


def wind_down(name="", seed="") -> str:
    """轻声哄睡的一段引导（按 seed 轮换）。"""
    call = (str(name) + "，") if name else ""
    g = _GUIDES[len(str(seed)) % len(_GUIDES)]
    return call + g


def breathing_478() -> str:
    """4-7-8 呼吸法，单拎出来。"""
    return ("跟我做 4-7-8 呼吸：鼻子吸气数到四，屏住数到七，"
            "嘴巴缓缓呼气数到八。重复四个回合，人就静下来了。")


def count_along(n=10) -> str:
    """数呼吸/数羊式的陪数。"""
    n = max(3, min(30, int(n)))
    return "我陪你数：" + "、".join(str(i) for i in range(1, n + 1)) + "……数着数着就睡着了。"
