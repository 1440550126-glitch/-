def provinces(config=None) -> list:
    db = dict(_SPECIALTY)
    if isinstance(config, dict) and isinstance(config.get("specialty"), dict):
        db.update(config["specialty"])
    return list(db)


def _table(config) -> dict:
    db = dict(_SPECIALTY)
    if isinstance(config, dict) and isinstance(config.get("specialty"), dict):
        for k, v in config["specialty"].items():
            if str(v).strip():
                db[str(k)] = str(v).strip()
    return db


def find_province(query, config=None) -> str:
    u = str(query or "")
    db = _table(config)
    best, blen = "", 0
    for p in db:
        if p in u and len(p) > blen:
            best, blen = p, len(p)
    for a, real in _ALIAS.items():
        if a in u and len(a) > blen and real in db:
            best, blen = real, len(a)
    return best


def about(province, config=None) -> str:
    db = _table(config)
    p = province if province in db else find_province(province, config)
    s = db.get(p)
    return f"{p}：{s}" if s else ""


def is_specialty_query(utterance, config=None) -> bool:
    u = str(utterance or "")
    if not find_province(u, config):
        return False
    return any(k in u for k in ("特产", "小吃", "有什么好吃", "有啥好吃", "名片", "出名的",
                                "有什么吃的", "有什么玩的", "有什么好玩", "好玩的", "土特产"))
