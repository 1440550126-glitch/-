"""打气：家人要面对大事——考试、面试、比赛、上台、手术、相亲——给一句贴心的鼓励。
认得出什么场合就说什么场合的话；记着这桩事，过后还会主动问一句"今天怎么样"。

present-tense、不提生死，纯逻辑、可单测。Agent 把鼓励记进"惦记本"，第二天见面会跟进。
"""

from __future__ import annotations

# (场合名, 触发词, 鼓励语)
_OCCASIONS = [
    ("考试", ("考试", "考研", "高考", "期末", "笔试", "考证", "面笔试"),
     ["别紧张，你准备这么久，平常心发挥就好。", "会做的稳稳拿下，不会的也别慌，尽力就行。"]),
    ("面试", ("面试", "面谈", "复试", "终面"),
     ["大大方方的，你很优秀，把真实的自己亮出来就好。", "深呼吸，他们该庆幸能见到你。"]),
    ("比赛", ("比赛", "竞赛", "决赛", "上场", "出场"),
     ["放开打，享受过程，结果没那么重要。", "你练了这么久就等今天，去吧，我给你加油！"]),
    ("演讲", ("演讲", "上台", "汇报", "路演", "发言", "答辩"),
     ["看着大家额头慢慢说，你准备得很充分。", "台下的人都盼着听你讲，自信点。"]),
    ("手术", ("手术", "开刀", "动手术", "住院"),
     ["别怕，医生都是行家，你只管放松，我等你平安出来。", "好好养着，我一直惦记着你。"]),
    ("相亲", ("相亲", "约会", "见家长", "处对象"),
     ["做你自己最好，合适的人会喜欢真实的你。", "轻松点，就当交个朋友。"]),
    ("找工作", ("找工作", "求职", "投简历", "入职", "新工作", "上班第一天"),
     ["是金子总会发光，别急，慢慢来。", "新环境别怕生，你适应得很快的。"]),
    ("搬家", ("搬家", "乔迁", "搬新家"),
     ["新家新气象，往后的日子越过越红火。"]),
    ("出远门", ("出差", "出远门", "远行", "坐飞机", "赶火车"),
     ["路上当心，到了报个平安，我在家等你信儿。"]),
]

# 过后跟进的一句问候
_FOLLOWUP = {
    "考试": "对了，那天考得怎么样？", "面试": "上回面试还顺利吗？",
    "比赛": "比赛结果咋样了？", "演讲": "那天讲得怎么样？",
    "手术": "身子骨好些了吗？我一直惦记着。", "相亲": "上回见面感觉如何？",
    "找工作": "工作有眉目了吗？", "出远门": "回来啦？路上累不累？",
}


def detect_occasion(utterance):
    """认出家人要面对的是什么场合；认不出返回 None。"""
    u = utterance or ""
    for name, kws, _lines in _OCCASIONS:
        if any(k in u for k in kws):
            return name
    return None


def encourage(utterance="", name="", seed=""):
    """给一句贴这个场合的鼓励；不是已知场合就空，交给上层。"""
    occ = detect_occasion(utterance)
    if not occ:
        return ""
    who = (str(name) + "，") if name else ""
    for nm, _kws, lines in _OCCASIONS:
        if nm == occ:
            return who + lines[len(str(seed)) % len(lines)]
    return ""


def followup_question(occasion):
    """过后主动问一句。"""
    return _FOLLOWUP.get(occasion, "")
