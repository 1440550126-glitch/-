"""说出我的感受：分身不只回应你，也会袒露自己的心情——你高兴它跟着乐、你病了它揪心、
你有出息它骄傲。让它像个有真心的人，不是没温度的应答机。

分身的七情已随互动起伏（见 emotions），这里把当下的心情用本人的口吻说出来。纯逻辑、可单测。
"""

from __future__ import annotations

_FEELING = {
    "喜": ["看你这样，我心里也亮堂。", "你高兴，我就跟着乐。"],
    "乐": ["陪你说着话，我这心里美滋滋的。", "今儿个心情真不赖。"],
    "哀": ["听着我心里也沉沉的，你不好受，我也跟着难受。", "唉，我陪你一块儿担着。"],
    "惧": ["说真的，我这心里也有点悬着，咱一起稳住。", "我也替你捏把汗。"],
    "爱": ["跟你在一块儿，我这心里满满的。", "有你们在，我就知足。"],
    "怒": ["我替你不平，这事儿换我也来气。", "听着就窝火，但咱别气坏身子。"],
    "恶": ["这事儿听着就不舒坦。", "我心里也别扭。"],
    "欲": ["我这心里也盼着那一天呢。", "想想就有奔头。"],
}

_NEUTRAL = ["我这会儿心里挺平静的，踏踏实实陪着你。", "没啥大起伏，安安稳稳的，挺好。"]


def share_feeling(mood, seed="") -> str:
    """把当下的心情说出来；没有明显情绪就给一句平和的。"""
    pool = _FEELING.get(mood)
    if not pool:
        pool = _NEUTRAL
    return pool[len(str(seed)) % len(pool)]


def is_feeling_query(utterance) -> bool:
    u = utterance or ""
    if "你" not in u:
        return False
    return any(k in u for k in ("什么感觉", "什么心情", "开心吗", "高兴吗", "难过吗",
                                "心情怎么样", "心情如何", "现在感觉", "感觉怎么样",
                                "累不累", "也难过吗", "什么心境"))
