def _all(config=None) -> dict:
    d = dict(_TOPICS)
    cfg = (config or {}).get("med_safety") if isinstance(config, dict) else None
    extra = (cfg or {}).get("topics") if isinstance(cfg, dict) else None
    if isinstance(extra, dict):
        for name, v in extra.items():
            if isinstance(v, (list, tuple)) and len(v) >= 2:
                d[str(name)] = (str(v[0]), str(v[1]))
            elif isinstance(v, dict) and v.get("say"):
                d[str(name)] = (str(v["say"]), str(v.get("tip", "")))
    return d


def topics(config=None) -> list:
    return list(_all(config).keys())


def find_topic(utterance, config=None):
    """认出问的哪个服药问题（别名最长匹配）。听不出返回 None。"""
    u = str(utterance or "")
    for word in sorted(_ALIAS, key=len, reverse=True):
        if word in u:
            return _ALIAS[word]
    for name in _all(config):
        if name in u:
            return name
    return None


def advise(topic, config=None) -> str:
    """某个服药问题的说法 + 提醒 + 免责。查不到返回空。"""
    d = _all(config)
    key = _ALIAS.get(str(topic or ""), str(topic or ""))
    if key not in d:
        return ""
    say, tip = d[key]
    return f"{say}" + (f"（{tip}）" if tip else "") + "（通用常识，具体以说明书/医嘱为准。）"


def is_med_safety_query(utterance, config=None) -> bool:
    """是不是在问服药常识（认出主题 + 怎么/能不能/还是 的意图）。"""
    u = str(utterance or "")
    if not find_topic(u, config):
        return False
    return any(k in u for k in ("怎么", "咋", "能不能", "能吗", "可以吗", "行吗", "还是", "多久",
                                "怎么办", "对不对", "要紧吗", "该", "吗", "注意"))


def count(config=None) -> int:
    return len(_all(config))
