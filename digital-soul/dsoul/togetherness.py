_ALIAS = {"喝口茶": "喝茶", "喝杯茶": "喝茶", "喝壶茶": "喝茶", "沏茶": "喝茶", "泡茶": "喝茶",
          "品茶": "喝茶", "遛弯": "散步", "遛遛": "散步", "走两步": "散步",
          "看夕阳": "夕阳", "看晚霞": "夕阳", "晚霞": "夕阳", "歇会儿": "坐会儿",
          "坐一会": "坐会儿", "晒晒": "晒太阳", "听听雨": "听雨", "看月亮": "赏月",
          "看月": "赏月", "星星": "看星星", "看星": "看星星"}

_CUES = ("陪我", "陪陪我", "陪着我", "一起", "咱俩", "咱们", "陪你", "陪着")


def activities() -> list:
    return list(_TOGETHER.keys())


def _find(utterance) -> str:
    u = str(utterance or "")
    best, blen = "", 0
    for k in _TOGETHER:
        if k in u and len(k) > blen:
            best, blen = k, len(k)
    for a, real in _ALIAS.items():
        if a in u and len(a) > blen:
            best, blen = real, len(a)
    return best


def accompany(utterance, name="", seed="") -> str:
    """认出要一起做的事，给一段在场陪伴的话；认不出给个通用的"我陪你"。"""
    act = _find(utterance)
    call = (str(name) + "，") if name else ""
    if act and act in _TOGETHER:
        return call + _TOGETHER[act]
    return call + "好，我陪你。啥也不用多说，我就在这儿，陪着你。"


def is_accompany_request(utterance) -> bool:
    u = str(utterance or "")
    if not any(c in u for c in _CUES):
        return False
    # 认得出一件可一起做的事 → 作伴；否则只接"纯陪着"（别抢"陪我动动脑/唱歌"等有专门去处的）
    if _find(u):
        return True
    return any(k in u for k in ("陪陪我", "陪着我", "陪我坐", "陪我待会", "陪我会儿",
                                "就陪陪", "陪我歇", "陪我待"))
