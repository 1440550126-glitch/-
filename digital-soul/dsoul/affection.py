"""真情流露：被问"你想我吗""你在乎我吗"，分身不打太极——真心实意地应你。
让爱说得出口。按关系给更贴心的话（老伴/孩子/父母各有各的暖）。present-tense、可单测。
"""

from __future__ import annotations

_ASK = ("你想我吗", "你想不想我", "你还想我吗", "你想我了吗", "你想我了没", "你想我没",
        "你在乎我吗", "你还在乎我吗", "你爱我吗", "你还爱我吗", "你喜欢我吗", "你心里有我吗",
        "你会想我吗", "你疼我吗", "你还疼我吗")

# 关系关键词 → 一句掏心窝的话
_BY_REL = [
    (("老伴", "老婆", "老公", "妻", "夫", "爱人"),
     ["想啊，咋能不想——你是我这辈子最舍不得的人。",
      "在乎得很，少了你，这屋子都没了魂儿。"]),
    (("儿", "女", "孙", "外孙", "娃", "闺女"),
     ["想啊，你是我的心头肉，搁哪儿我都惦记。",
      "疼你还用问？你好好的，我比啥都高兴。"]),
    (("爸", "妈", "父", "母", "爷", "奶"),
     ["想，咋不想——您养我这么大，我心里都记着呢。",
      "在乎您着呢，您身子骨硬朗，就是我最大的福气。"]),
]

_DEFAULT = ["想啊，咋不想，没见着你心里就空落落的。",
            "在乎，你在我心里分量重着呢，这还用问。",
            "想你想得紧，你一来我这心就踏实了。"]


def is_love_query(utterance) -> bool:
    u = utterance or ""
    return any(k in u for k in _ASK)


def love_reply(relation="", seed="") -> str:
    """真心实意应一句；按关系给更贴心的。"""
    rel = str(relation or "")
    s = str(seed)
    for kws, lines in _BY_REL:
        if any(k in rel for k in kws):
            return lines[len(s) % len(lines)]
    return _DEFAULT[len(s) % len(_DEFAULT)]
