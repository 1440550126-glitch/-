def _all_depts(config=None) -> list:
    items = list(_DEPTS)
    cfg = (config or {}).get("triage") if isinstance(config, dict) else None
    extra = (cfg or {}).get("depts") if isinstance(cfg, dict) else None
    if isinstance(extra, list):
        for d in extra:
            if isinstance(d, (list, tuple)) and len(d) >= 2:
                items.append((str(d[0]), list(d[1]), str(d[2]) if len(d) > 2 else ""))
            elif isinstance(d, dict) and d.get("dept"):
                items.append((str(d["dept"]), list(d.get("symptoms") or []), str(d.get("tip", ""))))
    return items


def emergency_signs(utterance):
    """命中危险信号就返回那条说明；否则 None。"""
    u = str(utterance or "")
    for keys, why in _RED_FLAGS:
        if any(k in u for k in keys):
            return why
    return None


def find_department(utterance, config=None):
    """按症状找科室，返回 (科室, 提示)；找不到返回 None。"""
    u = str(utterance or "")
    best = None
    best_len = 0
    for dept, keys, tip in _all_depts(config):
        for k in keys:
            if k and k in u and len(k) > best_len:   # 最长关键词优先，少误判
                best, best_len = (dept, tip), len(k)
    return best


def departments(config=None) -> list:
    """所有科室名（科普/兜底列举用）。"""
    return [d[0] for d in _all_depts(config)]


def advise(utterance, config=None) -> str:
    """一句话导诊：先查危险信号（让去急诊），否则给科室 + 提示 + 免责。"""
    why = emergency_signs(utterance)
    if why:
        return (f"⚠️ 这个情况（{why}）别挂号慢慢等了——立刻去急诊，或直接打 120。"
                "宁可白跑一趟，也别耽误。我陪着你，别慌。")
    hit = find_department(utterance, config)
    if not hit:
        return ("听你说的，我一时拿不准挂哪个科。这种情况挂「全科医学科」或「普通内科」最稳，"
                "让医生先看看再分诊。要是越来越重，就直接去急诊。")
    dept, tip = hit
    base = f"这种情况一般挂「{dept}」。"
    if tip:
        base += tip
    return base + " （我不是医生，这只是帮你找对窗口；拿不准或加重了，挂急诊或打 120。）"


def is_triage_query(utterance, config=None) -> bool:
    """是不是在问「挂什么科 / 看病去哪个科」，或描述了症状想找科室。"""
    u = str(utterance or "")
    asks = any(k in u for k in ("挂什么科", "挂哪个科", "看什么科", "哪个科", "挂号挂", "看病挂",
                                "该挂", "去哪个科", "挂啥科", "什么科室", "导诊", "分诊"))
    if asks:
        return True
    # 危险信号一律接住（哪怕没说"看病"，也要喊去急诊；前面更专的应急路由没接到的兜在这）
    if emergency_signs(u):
        return True
    # 描述了症状 + 求医意图（"胃疼该看哪""头晕去医院看啥"）
    if find_department(u, config):
        if any(k in u for k in ("挂", "看", "医院", "就医", "去哪", "看哪", "找哪", "该看")):
            return True
    return False


def count(config=None) -> int:
    return len(_all_depts(config))
