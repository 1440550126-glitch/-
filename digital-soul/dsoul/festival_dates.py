def nudge(name, days) -> str:
    """主动提一句节日的暖话：当天送祝福，临近则张罗。算不出返回空。"""
    fn = canonical(name)
    if not fn or days is None or days < 0:
        return ""
    bless, suggest = _WARMTH.get(fn, ("节日快乐", ""))
    if days == 0:
        return f"今儿是{fn}，{bless}！" if bless else f"今儿{fn}，{suggest}。"
    if days <= 7:
        tip = ("，" + suggest) if suggest else ""
        return f"还有 {days} 天就{fn}了{tip}。"
    return ""
