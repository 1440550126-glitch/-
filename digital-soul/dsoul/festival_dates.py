def known_festivals() -> list:
    return list(_LUNAR.keys()) + list(_FIXED.keys())


def canonical(name) -> str:
    """把"过年/月饼节/十一"等都归到正名；不是节日返回空。"""
    n = str(name or "").strip()
    if not n:
        return ""
    if n in _LUNAR or n in _FIXED:
        return n
    if n in _ALIAS:
        return _ALIAS[n]
    # 宽松包含："离过年还有几天" 里截出的"过年"
    for k, v in _ALIAS.items():
        if k in n:
            return v
    for k in list(_LUNAR) + list(_FIXED):
        if k in n:
            return k
    return ""


def festival_date(name, year):
    """某年该节日的阳历日期 (month, day)；查不到返回 None。"""
    fn = canonical(name)
    if not fn:
        return None
    if fn in _FIXED:
        return _FIXED[fn]
    return _LUNAR.get(fn, {}).get(int(year))


def days_to_festival(name, now=None):
    """到下一个该节日还有几天；今年已过就看明年。算不出返回 None。"""
    fn = canonical(name)
    if not fn:
        return None
    now = now or datetime.now()
    today = now.date() if isinstance(now, datetime) else now
    for y in (today.year, today.year + 1):
        md = festival_date(fn, y)
        if not md:
            continue
        try:
            target = date(y, md[0], md[1])
        except ValueError:
            continue
        if target >= today:
            return (target - today).days
    return None


def describe(name, now=None) -> str:
    """一句话倒计时。算不出返回空。"""
    fn = canonical(name)
    d = days_to_festival(name, now)
    if d is None:
        return ""
    if d == 0:
        return f"就是今天！{fn}到了，节日快乐。"
    return f"离{fn}还有 {d} 天。"


