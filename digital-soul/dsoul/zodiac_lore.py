def animals() -> list:
    return list(_ANIMALS)


def signs() -> list:
    return list(_SIGNS)


def find_animal(query) -> str:
    u = str(query or "")
    for a, real in _ANIMAL_ALIAS.items():
        if a in u:
            return real
    import re
    m = re.search(r"属([鼠牛虎兔龙蛇马羊猴鸡狗猪])", u)
    if m:
        return m.group(1)
    for a in _ANIMALS:
        if a in u:
            return a
    return ""


def find_sign(query) -> str:
    u = str(query or "")
    for s in _SIGNS:
        if s in u or s[:-1] in u:                   # "天蝎"也认
            return s
    return ""


def animal_traits(animal) -> str:
    a = animal if animal in _ANIMALS else find_animal(animal)
    row = _ANIMALS.get(a)
    if not row:
        return ""
    return f"属{a}的：{row[0]} {row[1]}"


def sign_traits(sign) -> str:
    s = sign if sign in _SIGNS else find_sign(sign)
    row = _SIGNS.get(s)
    if not row:
        return ""
    return f"{s}（{row[0]}）：{row[1]}"


def is_zodiac_lore_query(utterance) -> bool:
    u = str(utterance or "")
    has = find_animal(u) or find_sign(u)
    if not has:
        return False
    return any(k in u for k in ("性格", "脾气", "脾性", "特点", "相配", "配对", "合不合",
                                "般配", "合得来", "什么样的人", "怎么样的人", "啥性格"))
