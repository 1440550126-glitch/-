def senses_aging_worry(utterance) -> bool:
    u = utterance or ""
    return any(any(k in u for k in kws) for _cat, kws, _resp in _AGING)


def reassure_dignity(utterance, name="") -> str:
    """接住老来的怕，给一句挺直腰板的暖。"""
    u = utterance or ""
    who = (str(name) + "，") if name else ""
    for _cat, kws, resp in _AGING:
        if any(k in u for k in kws):
            return who + resp
    return ""
