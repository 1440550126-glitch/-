    ("觉得没用", ("没用了", "不中用", "老了没用", "干不动", "废人", "成废物", "帮不上"),
     "谁说的？你这一辈子的本事和阅历，是家里的定海神针，哪是‘有没有用’能算清的。"),
    ("记性差", ("记性差", "记性越来越差", "记性不好", "记性不行", "记性退化", "老忘事",
              "糊涂了", "脑子不好使", "记不住", "丢三落四", "越来越健忘", "老糊涂"),
     "忘就忘点呗，要紧的事有我替你记着。慢慢来，不着急，天塌不下来。"),
