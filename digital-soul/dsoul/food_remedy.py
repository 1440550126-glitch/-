def detect(utterance):
    u = utterance or ""
    for name, kws, _adv in _REMEDY:
        if any(k in u for k in kws):
            return name
    return None


def is_remedy_query(utterance) -> bool:
    u = utterance or ""
    return detect(u) is not None and any(
        k in u for k in ("吃什么", "吃点什么", "喝什么", "食疗", "怎么调理", "吃啥好",
                         "喝点啥", "吃什么好", "调理", "食补"))


def advice(utterance) -> str:
    u = utterance or ""
    for _name, kws, adv in _REMEDY:
        if any(k in u for k in kws):
            return adv
    return ""
