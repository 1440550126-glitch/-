def themes() -> list:
    return list(_PROVERBS.keys())


def for_theme(theme) -> list:
    return list(_PROVERBS.get(theme, []))


def match_theme(utterance):
    """问话/话题落在哪个主题；命中关键词最多的主题优先。"""
    u = utterance or ""
    best, score = None, 0
    for theme, kws in _TOPIC.items():
        c = sum(1 for k in kws if k in u)
        if c > score:
            best, score = theme, c
    return best


def proverb_for(utterance="", seed="") -> str:
    """聊到什么，顺口来一句应景的老话。没沾边的话题返回空。"""
    theme = match_theme(utterance)
    if not theme:
        return ""
    items = _PROVERBS[theme]
    p = items[len(str(seed or utterance)) % len(items)]
    return f"老话说得好，{p}"


def recite(theme=None, k=3) -> str:
    """背几句某主题的老话（不指定主题就挑'做人'）。"""
    theme = theme if theme in _PROVERBS else "做人"
    items = _PROVERBS[theme][:k]
    return f"说到{theme}，老人常念叨：" + " / ".join(items) + "。" if items else ""
