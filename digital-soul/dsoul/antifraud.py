def _near_hit(near, u) -> bool:
    if not near:
        return False
    a, b = near
    return any(x in u for x in a) and any(y in u for y in b)


def scam_kind(utterance) -> str:
    """闻一闻是哪类骗局；不像就返回空。"""
    u = str(utterance or "")
    for s in _SCAMS:
        if any(k in u for k in s["any"]) or _near_hit(s.get("near"), u):
            return s["kind"]
    return ""


def smells_like_scam(utterance) -> bool:
    return scam_kind(utterance) != ""


def _entry(kind):
    for s in _SCAMS:
        if s["kind"] == kind:
            return s
    return None


def warn(kind="", name="", utterance="") -> str:
    """拦一句：戳穿 + 缘由 + 三条当下能做的。kind 没给就从话里认。"""
    kind = kind or scam_kind(utterance)
    s = _entry(kind)
    if not s:
        return ""
    call = (name + "，") if name else ""
    return f"{call}打住！{s['hit']} {_ACTIONS}"


def is_fraud_question(utterance) -> bool:
    """直接来问'这是不是骗子/会不会是诈骗'。"""
    u = str(utterance or "")
    return any(k in u for k in ("是不是骗子", "是不是诈骗", "会不会是骗", "是骗子吗",
                                "是诈骗吗", "这靠谱吗是不是骗", "反诈", "防诈骗"))


