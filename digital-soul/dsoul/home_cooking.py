def dishes(config=None) -> list:
    return list(_merge(config).keys())


def _merge(config) -> dict:
    db = {k: dict(v) for k, v in _DISHES.items()}
    if isinstance(config, dict) and isinstance(config.get("home_cooking"), dict):
        for name, d in config["home_cooking"].items():
            if isinstance(d, dict):
                db[name] = {
                    "用料": str(d.get("用料", "")),
                    "步骤": [str(s) for s in (d.get("步骤") or [])],
                    "窍门": str(d.get("窍门", "")),
                }
    return db


def find(query, config=None) -> str:
    """从一句话里认出是哪道菜（含别名）。认不出返回空。"""
    u = str(query or "")
    db = _merge(config)
    best, blen = "", 0
    for name in db:
        if name in u and len(name) > blen:
            best, blen = name, len(name)
    for a, real in _ALIAS.items():
        if a in u and len(a) > blen and real in db:
            best, blen = real, len(a)
    return best


def how_to(dish, config=None) -> str:
    """把一道菜排成"用料 + 做法 + 窍门"。认不出返回空。"""
    db = _merge(config)
    name = dish if dish in db else find(dish, config)
    d = db.get(name)
    if not d:
        return ""
    steps = "；".join(f"{i}.{s}" for i, s in enumerate(d["步骤"], 1))
    out = f"{name}——\n用料：{d['用料']}\n做法：{steps}"
    if d.get("窍门"):
        out += f"\n窍门：{d['窍门']}"
    return out


def is_cooking_howto(utterance, config=None) -> bool:
    u = str(utterance or "")
    if not find(u, config):
        return False
    return any(k in u for k in ("怎么做", "咋做", "怎么烧", "怎么炒", "怎么煮", "做法",
                                "步骤", "教我做", "怎么弄", "咋整"))
