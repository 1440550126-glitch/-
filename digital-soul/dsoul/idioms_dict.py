def _table(config) -> dict:
    db = dict(_DICT)
    if isinstance(config, dict) and isinstance(config.get("idioms"), dict):
        for k, v in config["idioms"].items():
            if isinstance(v, (list, tuple)) and v:
                db[str(k).strip()] = (str(v[0]), str(v[1]) if len(v) > 1 else "")
            elif isinstance(v, str) and v.strip():
                db[str(k).strip()] = (v.strip(), "")
    return db


def idioms(config=None) -> list:
    return list(_table(config).keys())


def find(query, config=None) -> str:
    u = str(query or "")
    for w in _table(config):
        if w in u:
            return w
    return ""


def explain(idiom, config=None) -> str:
    """成语释义＋用法。认不出返回空。"""
    db = _table(config)
    w = idiom if idiom in db else find(idiom, config)
    row = db.get(w)
    if not row:
        return ""
    meaning, usage = row
    return f"{w}：{meaning}" + (f"（{usage}）" if usage else "")


def is_idiom_lookup(utterance, config=None) -> bool:
    u = str(utterance or "")
