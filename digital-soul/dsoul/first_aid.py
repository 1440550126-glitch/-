def detect(utterance):
    u = utterance or ""
    for name, kws, _adv in _FIRSTAID:
        if any(k in u for k in kws):
            return name
    return None


def is_firstaid_query(utterance) -> bool:
    u = utterance or ""
    return detect(u) is not None and any(
        k in u for k in ("怎么办", "咋办", "怎么处理", "如何处理", "怎么弄", "咋整", "怎么急救"))


def advice(utterance) -> str:
    u = utterance or ""
    for _name, kws, adv in _FIRSTAID:
        if any(k in u for k in kws):
            return adv
    return ""
