_FAMILY_NAMES = set(_SURNAMES)


def surnames(config=None) -> list:
    db = dict(_SURNAMES)
    if isinstance(config, dict) and isinstance(config.get("surnames"), dict):
        db.update(config["surnames"])
    return list(db)


def _table(config) -> dict:
    db = dict(_SURNAMES)
    if isinstance(config, dict) and isinstance(config.get("surnames"), dict):
        for k, v in config["surnames"].items():
            if isinstance(v, (list, tuple)) and len(v) >= 2:
                db[str(k)] = (str(v[0]), str(v[1]))
    return db


def find_surname(query, config=None) -> str:
    """从"我姓李""张姓的来历"里取出那个姓。取不到返回空。"""
    import re
    u = str(query or "")
    db = _table(config)
    m = re.search(r"姓([一-龥])", u) or re.search(r"([一-龥])姓", u)
    if m and m.group(1) in db:
        return m.group(1)
    for s in db:                                    # 兜底：句中出现的姓
        if s in u:
            return s
    return ""


def about(surname, config=None) -> str:
    """讲讲这个姓：源流 + 名人。认不出返回空。"""
    db = _table(config)
    s = surname if surname in db else find_surname(surname, config)
    row = db.get(s)
    if not row:
        return ""
    origin, people = row
    return f"「{s}」姓：{origin} 历史名人有{people}等。"


def is_surname_query(utterance, config=None) -> bool:
    u = str(utterance or "")
    if not find_surname(u, config):
        return False
    return any(k in u for k in ("姓的来历", "姓的起源", "姓怎么来", "姓的由来", "这个姓",
                                "姓氏起源", "姓出过", "姓的名人", "姓打哪", "讲讲我的姓",
                                "姓从哪", "姓有什么", "姓的典故"))
