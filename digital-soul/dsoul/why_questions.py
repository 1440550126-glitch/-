def _all(config) -> list:
    items = list(_WHY)
    if isinstance(config, dict) and isinstance(config.get("why"), list):
        for it in config["why"]:
            if isinstance(it, dict) and it.get("keys") and it.get("answer"):
                keys = it["keys"] if isinstance(it["keys"], list) else [it["keys"]]
                items = [([str(k) for k in keys], str(it["answer"]))] + items
    return items


def find_question(query, config=None):
    u = str(query or "")
    if not u:
        return None
    best, blen = None, 0
    for keys, ans in _all(config):
        for k in keys:
            if k and k in u and len(k) > blen:
                best, blen = ans, len(k)
    return best


def answer(query, config=None) -> str:
    return find_question(query, config) or ""


def count() -> int:
    return len(_WHY)


def is_why_query(utterance, config=None) -> bool:
    return find_question(utterance, config) is not None
