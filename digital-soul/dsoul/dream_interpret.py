_TRIGGER = ("梦见", "梦到", "做梦", "做了个梦")
_QUERY = ("什么意思", "啥意思", "啥兆头", "什么兆头", "解梦", "好不好", "代表什么",
          "什么征兆", "啥征兆", "怎么回事")


def is_dream_query(utterance) -> bool:
    u = utterance or ""
    return any(t in u for t in _TRIGGER) and any(q in u for q in _QUERY)


def interpret(utterance) -> str:
    """认出梦里的意象，给个宽心说法；认不出也给句安心话。"""
    u = str(utterance or "")
    for key in sorted(_DREAMS, key=len, reverse=True):
        if key in u:
            return _DREAMS[key] + "（图个乐呵，别太当真哈。）"
    return "梦嘛，多是日有所思——白天惦记啥，夜里就梦见啥。别多想，宽心就好。"
